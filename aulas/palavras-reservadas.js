//Aula: Diferença de var, let e const

//var > é parecido com o let, mas o var pode ser chamado através do "console.log"
//antes mesmo de ter sido declarada um valor para essa variável
//o retorno no console será undefined (não definido)
//let > a variável não pode ser chamada antes de ser inicializada

//REGRINHA:

//let > utilizada para declarar variáveis mutáveis, ou seja, cujo conteúdo pode
//ser alterado posteriormente

//const > utilizada para declarar variáveis imutáveis, ou seja, cujo conteúdo
//não pode ser mais alterado posteriormente

